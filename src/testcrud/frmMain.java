    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            String NIS = tblData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM `t_siswa` WHERE NIS = '"+NIS+"'";
            int status = 0;
            try {
                status = KoneksiDB.execute(SQL);
            } catch (SQLException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if("".equals(txNIS.getText()) || "".equals(txAlamat.getText()) || "".equals(txKelas.getText()) || "".equals(txNama.getText()) || "".equals(txEmail.getText()) || "".equals(txHoby.getText()) || "".equals(txUmur.getText()) || "".equals(txCita.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                String JK = "";
                if(rdLaki.isSelected()){
                    JK = "L";
                }else {
                    JK = "P";
                }
                String SQL = "INSERT INTO `t_siswa` (`NIS`,`NamaSiswa`,`JenisKelamin`,`Kelas`,`Email`,`Alamat`,`Hoby`,`Umur`,`CitaCita`)" + "VALUES('"+txNIS.getText()+"','"+txNama.getText()+"','"+ JK +"','"+txKelas.getText()+"','"+txEmail.getText()+"','"+txAlamat.getText()+"','"+txHoby.getText()+"','"+txUmur.getText()+"','"+txCita.getText()+"')";
                int status = KoneksiDB.execute(SQL);
                if(status == 1){
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    selectData();
                }else{
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
                }   } catch (SQLException ex) {
                    Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
    }//GEN-LAST:event_btnAddActionPerformed

